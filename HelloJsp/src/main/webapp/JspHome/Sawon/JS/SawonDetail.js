// 생일 입력 필드
function BDateMask(Target)
{
	// -----------------------------------------------------------------
	// https://gurtn.tistory.com/86
	// https://codencoding.co.kr/정규표현식이란/
	// 0000-00-00								: (4)-(2)-(2)
	// .replace(/[^0-9]/g, '');					: ^...	- 숫자를 제외한
	//											: g		- 모든 문자 검색
	// .replace(/^(\d{4})(\d{2})(\d{2})$/g,		: ^()	- 시작그룹
	//			'$1-$2-$3');					: \d	- 숫자 == [0-9]
	//											: {}	- 자리수
	//											: $		- 끝그룹
	// -----------------------------------------------------------------
	Target.value = Target.value
						.replace(/[^\d]/g, '')
						.replace(/^(\d{4})(\d{2})(\d{2})$/g, '$1/$2/$3');
}
// 숫자 입력 필드
function NumberMask(Target)
{
	// -----------------------------------------------------------------
	// .replace(/[^0-9]/g, '');					: ^...	- 숫자를 제외한
	//											: g		- 모든 문자 검색
	// -----------------------------------------------------------------
	Target.value = Target.value
						.replace(/[^\d]/g, '');
}
// 전화 입력 필드
function PhoneMask(Target)
{
	// -----------------------------------------------------------------
	// https://gurtn.tistory.com/86
	// https://codencoding.co.kr/정규표현식이란/
	// 00-000-0000 or 000-0000-0000	or 0000-0000-0000	: (2~4)-(3~4)-(4)
	// .replace(/[^0-9]/g, '');							: ^...	- 숫자를 제외한
	//													: g		- 모든 문자 검색
	// .replace(/^(\d{2,3})(\d{3,4})(\d{4})$/g,			: ^()	- 시작그룹
	//			'$1-$2-$3');							: \d	- 숫자 == [0-9]
	//													: {}	- 자리수 (최소,최대)
	//													: $		- 끝그룹
	// -----------------------------------------------------------------
	Target.value = Target.value
						.replace(/[^\d]/g, '')
						.replace(/^(\d{2,4})(\d{3,4})(\d{4})$/g, '$1-$2-$3');
}
